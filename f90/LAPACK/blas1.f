      SUBROUTINE Median(X,N,XMED)
  
      Integer N,N2
      Real*8  X(*),XMED,TWO

      TWO = 2.0
      Call Sort(N,X)
      N2 = N/2
      if (2*N2.eq.N) then
        XMED = (X(N2)+X(N2+1))/TWO
      else
        XMED = X(N2+1)
      endif

      RETURN

      END ! Subroutine Median()

